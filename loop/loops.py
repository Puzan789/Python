#while loops
b=int(input("enter the number to print"))
i=1
while(i<=b):
    print(i)
    i+=1
#for loop
b=int(input("enter the number to print"))
for num in b:
    print(num)
#using range
for i in range(1,10):
    print(i)
#string
a = "Pythonfav"
for letter in a:
    print('Current Letter:', letter)

#lists
a = [1, 2, 3]
b = ['Python', 'JavaScript', 'C++']

for i in a:
    print('Current Element:', i)

for language in b:
    print('Current language:', language)


